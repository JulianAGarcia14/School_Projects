/*
 * allOddBits - return 1 if all odd-numbered bits in word set to 1
 *   Examples allOddBits(0xFFFFFFFD) = 0, allOddBits(0xAAAAAAAA) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int allOddBits(int x) {
        // build mask: 0xAAAAAAAA
        int mask = (0xAA << 8) + 0xAA;
        mask = (mask << 16) + mask;

        return !((x & mask) ^ mask);
}
/*
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
        return (x >> (n << 3)) & 0xFF;
}
/*
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
        /* xor the nth byte and  then shift left to nth byte and xor the original number.
        Now the nth byte stores the information of mth byte because we xor nth byte twice. */

        int y = 0;
        n = n<<3; // n == n*3
        m = m<<3; //m == n*3
        y = 0xff & ((x>>n) ^ (x>>m)); // 1111 1111
        x = x ^ (y<<n);
        x = x ^ (y<<m);
        return x;
}
