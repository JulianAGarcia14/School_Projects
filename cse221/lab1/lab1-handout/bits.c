/*
 * replaceByte(x,n,c) - Replace byte n in x with c
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: replaceByte(0x12345678,1,0xab) = 0x1234ab78
 *   You can assume 0 <= n <= 3 and 0 <= c <= 255
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 3
 */
int replaceByte(int x, int n, int c) {
        int mask, shift;
        mask = 255; // byte-wide mask of 1s
        shift = n << 3; // == n*2^3 == n * 8
        mask = ~(mask << shift); // shift byte-wide mask, flip to get byte-wide mask of 0s
        c = c << shift; // shift bits of c into nth byte
        return (x & mask) | c; // apply mask to x, zeroing x's nth byte, replacing it with byte given by c
}
/*
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
        int sign = 1 << 31;
        int upperBound = ~(sign | 0x39); /*if > 0x39 is added, result goes negative*/
        int lowerBound = ~0x30;/*when < 0x30 is added, result is negative*/

        /*now add x and check the sign bit for each*/
        upperBound = sign & (upperBound+x) >> 31;
        lowerBound = sign & (lowerBound+1+x) >> 31;
        /*if either result is negative, it is not in desired range*/
        return !(upperBound | lowerBound);
}
/*
 * bitMask - Generate a mask consisting of all 1's
 *   lowbit and highbit
 *   Examples: bitMask(5,3) = 0x38
 *   Assume 0 <= lowbit <= 31, and 0 <= highbit <= 31
 *   If lowbit > highbit, then mask should be all 0's
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int bitMask(int highbit, int lowbit) {
	return ((2 << highbit) + ~0) >> lowbit << lowbit;
}
/*
 * rotateRight - Rotate x to the right by n
 *   Can assume that 0 <= n <= 31
 *   Examples: rotateRight(0x87654321,4) = 0x76543218
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 25
 *   Rating: 3
 */
int rotateRight(int x, int n) {
        /*shifts x by 32-n positions in order to get n bits to the front of the number.
        *Creates a mask of tmax and shifts that right by n-1 to create a mask that will
        *remove the bits where the new bits will go. shift x by n to move the number over
        *and and with mask to leave space for shifted 'rotated' bits. or the two together
        *to get result
        */
        int movingPart = x << (32 + (~n +1));
        int tmax = ~(1 << 31);
        tmax = tmax >> (n+((~1)+1));
        x = x >> n;
        x = x & tmax;
        return x | movingPart;
}
